import fs from "node:fs";
import path from "node:path";
import { summarizeCsvEntity } from "./field-inference.mjs";

const GENERATED_NOTE = "Generated by SpecWise deterministic draft generator. This draft does not use AI and requires human review.";
const GENERATED_NOTE_ZH = "本草案由 SpecWise 确定性规则生成，未调用 AI，需要人工审查。";

const HIGH_RISK_KEYWORDS = /permission|export|approve|approval|delete|audit|role|scope|configure|mark inactive|reassign|cross-team|cross-department|cross-company|权限|导出|审批|删除|审计|角色|范围|配置|停用|跨部门/i;
const WORKFLOW_KEYWORDS = /workflow|submit|submitted|review|reviewed|approve|approved|reject|rejected|edit|draft|closed|reopened|流程|提交|审核|通过|退回|编辑|草稿|关闭|重开/i;
const UNCLEAR_KEYWORDS = /unclear|not sure|needs confirmation|should be confirmed|不清楚|不确定|需要确认|是否|好像|maybe|requires approval|who can|whether/i;

function slug(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "") || "unknown";
}

function splitBilingualLabel(value) {
  const [name, nameZh] = String(value).split("/").map((part) => part.trim());
  return {
    name: name || value.trim(),
    nameZh: nameZh || name || value.trim()
  };
}

function normalizeModuleName(value) {
  return splitBilingualLabel(value.replace(/^\d+[_-]?/, "").replace(/\.mock\.txt$/i, "")).name;
}

function normalizeFileStem(fileName) {
  return fileName
    .replace(/\.mock\.txt$/i, "")
    .replace(/\.[^.]+$/, "")
    .replace(/^\d+[_-]?/, "");
}

function getEvidenceSourceType(type) {
  if (type === "mock_screenshot" || type === "image") return "screenshot";
  if (type === "markdown") return "notes";
  if (type === "text") return "text";
  if (type === "csv") return "csv";
  if (type === "xlsx") return "xlsx";
  return "material";
}

function getEvidenceTopic(material) {
  if (material.type === "csv") return "headers";
  if (material.type === "mock_screenshot" && material.signals?.screenTitle) return material.signals.screenTitle;
  const heading = material.signals?.headings?.[0];
  if (heading) return heading;
  return "summary";
}

function getMaterialSourceType(type) {
  if (type === "mock_screenshot" || type === "text") return "txt";
  if (type === "image") return "screenshot";
  if (type === "markdown") return "markdown";
  if (type === "csv") return "csv";
  if (type === "xlsx") return "xlsx";
  return null;
}

function resolveText(inputFolder, material) {
  if (!["mock_screenshot", "markdown", "text", "csv"].includes(material.type)) return "";
  const fullPath = path.resolve(process.cwd(), inputFolder, material.path);
  return fs.existsSync(fullPath) ? fs.readFileSync(fullPath, "utf8") : "";
}

function getLines(text) {
  return text.split(/\r?\n/);
}

function getBulletsUnder(text, heading) {
  const lines = getLines(text);
  const results = [];
  let active = false;

  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed.toLowerCase() === `${heading.toLowerCase()}:`) {
      active = true;
      continue;
    }

    if (!active) continue;
    if (!trimmed) {
      if (results.length > 0) break;
      continue;
    }
    if (/^[A-Z][A-Za-z ]+:$/.test(trimmed) || /^Visible .+:$/.test(trimmed) || /^Detected .+:$/.test(trimmed)) {
      break;
    }
    if (trimmed.startsWith("- ")) {
      results.push(trimmed.replace(/^- /, "").trim());
    }
  }

  return results;
}

function getUnclearLines(text) {
  return getLines(text)
    .map((line) => line.trim().replace(/^- /, ""))
    .filter((line) => line.length > 0 && !/^#+\s+/.test(line) && !/^unclear rules:?$/i.test(line) && !/^unclear:?$/i.test(line) && UNCLEAR_KEYWORDS.test(line));
}

function getBulletsUnderAny(text, headings) {
  return headings.flatMap((heading) => getBulletsUnder(text, heading));
}

function parseSimpleCsvLine(line) {
  return line.split(",").map((value) => value.trim());
}

function getCsvColumnValues(text, columnName) {
  const rows = getLines(text).filter((line) => line.trim().length > 0).map(parseSimpleCsvLine);
  if (rows.length < 2) return [];
  const headers = rows[0].map((header) => header.toLowerCase());
  const index = headers.indexOf(columnName.toLowerCase());
  if (index === -1) return [];
  return rows.slice(1).map((row) => row[index]).filter(Boolean);
}

function normalizeWorkflowStateLabel(value) {
  const normalized = String(value)
    .trim()
    .replace(/[`：:。.;,，]+$/g, "")
    .replace(/^[-*]\s*/, "")
    .replace(/^(->|→|↘)\s*/, "")
    .replace(/\s+/g, " ");

  const lower = normalized.toLowerCase();
  const known = new Map([
    ["草稿", "draft"],
    ["已提交", "submitted"],
    ["审核中", "reviewed"],
    ["已通过", "approved"],
    ["已退回", "rejected"],
    ["manager reviewed", "manager_reviewed"],
    ["follow-up draft", "follow_up_draft"],
    ["new customer", "new_customer"]
  ]);
  return known.get(normalized) ?? known.get(lower) ?? slug(lower);
}

function extractWorkflowStatesFromText(text) {
  const states = [];
  const lines = getLines(text);
  let previousStateCandidate = null;

  function addState(rawValue) {
    const state = normalizeWorkflowStateLabel(rawValue);
    if (state && state !== "unknown" && !/^(notes|unclear|known_states|known_actions|primary_customer_follow_up_workflow)$/.test(state)) {
      states.push(state);
    }
  }

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || /^```/.test(trimmed)) continue;

    if (/(?:->|→|↘)/.test(trimmed)) {
      if (/^(?:->|→|↘)/.test(trimmed) && previousStateCandidate) addState(previousStateCandidate);
      for (const part of trimmed.split(/\s*(?:->|→|↘)\s*/).filter(Boolean)) {
        addState(part);
      }
      previousStateCandidate = null;
      continue;
    }

    if (!trimmed.endsWith(":") && !trimmed.startsWith("- ")) {
      previousStateCandidate = trimmed;
    }
  }

  return states;
}

function inferFieldType(header) {
  const value = header.toLowerCase();
  if (/(submitted_at|reviewed_at|created_at|updated_at|_time|time$)/.test(value)) return "datetime";
  if (/(date|joined_at|period)/.test(value)) return "date";
  if (/score|amount|count|total|number/.test(value)) return "number";
  if (/status|state|role|scope|type|level/.test(value)) return "enum";
  if (/_id$/.test(value) && value !== "id") return "relation";
  return "string";
}

function inferRoleScope(roleName) {
  const value = roleName.toLowerCase();
  if (/president|super|global|总裁/.test(value)) return "global";
  if (/company|公司/.test(value)) return "company";
  if (/department|部门/.test(value)) return "department";
  if (/group|组/.test(value)) return "group";
  if (/viewer|read|只读/.test(value)) return "self";
  return "custom";
}

function inferQuestionCategory(text) {
  const value = String(text).toLowerCase();
  if (/export|导出/.test(value)) return "export";
  if (/approval|approve|requires approval|审批|批准/.test(value)) return "approval";
  if (/assignment|reassign|owner|分配|重新分配/.test(value)) return "assignment";
  if (/permission|role|scope|configure|cross-team|cross-department|cross-company|inactive|deactivate|who can|visibility|权限|角色|范围|配置|跨团队|跨部门|停用/.test(value)) return "permission";
  if (/workflow|reject|submit|edit|review|closed|reopened|overdue|reminder|流程|退回|提交|编辑|审核|关闭|重开|提醒/.test(value)) return "workflow";
  if (/audit|log|操作日志|审计/.test(value)) return "audit";
  if (/status|field|data|import|denominator|segment|状态|字段|数据|导入/.test(value)) return "data";
  return "data";
}

function inferQuestionPriority(text) {
  if (HIGH_RISK_KEYWORDS.test(text)) return "high";
  if (WORKFLOW_KEYWORDS.test(text)) return "medium";
  return "low";
}

function inferPermissionAction(text) {
  const value = text.toLowerCase();
  if (/export|导出/.test(value)) return "export";
  if (/import|导入/.test(value)) return "import";
  if (/configure|配置/.test(value)) return "configure";
  if (/approve|approval|通过|审批/.test(value)) return "approve";
  if (/edit|编辑/.test(value)) return "edit";
  if (/create|新增|创建/.test(value)) return "create";
  if (/delete|删除|deactivate|停用/.test(value)) return "delete";
  if (/view|查看|read/.test(value)) return "view";
  return "unknown";
}

function isLikelyRoleHeading(heading) {
  return heading.includes("/") || /President|Admin|Manager|Leader|Evaluator|Viewer|Director|Rep|System Admin|Sales/i.test(heading);
}

function createEvidenceItems(materials) {
  const evidenceItems = [];
  const evidenceByMaterial = new Map();

  for (const material of materials) {
    const sourceType = getMaterialSourceType(material.type);
    if (!sourceType) continue;

    const sourceMaterialId = `src_${material.id}`;
    const summaryId = `ev_${getEvidenceSourceType(material.type)}_${slug(normalizeFileStem(material.fileName))}_${slug(getEvidenceTopic(material))}`;
    evidenceItems.push({
      id: summaryId,
      sourceMaterialId,
      type: material.type === "csv" ? "table_header" : material.type === "mock_screenshot" ? "ui_text" : "business_note",
      quoteOrDescription: material.summary,
      location: {
        file: material.path,
        page: null,
        sheet: null,
        row: null,
        column: null
      },
      reliability: material.type === "unsupported" ? "low" : "medium"
    });

    evidenceByMaterial.set(material.id, [summaryId]);
  }

  return { evidenceItems, evidenceByMaterial };
}

function createSourceMaterials(materials) {
  return materials
    .map((material) => {
      const type = getMaterialSourceType(material.type);
      if (!type) return null;
      return {
        id: `src_${material.id}`,
        type,
        path: material.path,
        language: "unknown",
        summary: material.summary,
        detectedSignals: Object.keys(material.signals ?? {})
      };
    })
    .filter(Boolean);
}

function buildModuleAndPages({ materials, texts, evidenceByMaterial }) {
  const modules = new Map();
  const pages = [];

  function ensureModule(rawName, material, confidence = "medium") {
    const label = splitBilingualLabel(normalizeModuleName(rawName));
    const key = slug(label.name);
    const id = `mod_${key}`;
    const evidenceIds = evidenceByMaterial.get(material.id) ?? [];

    if (!modules.has(id)) {
      modules.set(id, {
        id,
        name: label.name,
        nameZh: label.nameZh,
        description: `Deterministically detected module candidate from input material: ${material.path}.`,
        pages: [],
        confidence,
        evidenceIds,
        needsReview: true
      });
    } else {
      const existing = modules.get(id);
      existing.evidenceIds = Array.from(new Set([...existing.evidenceIds, ...evidenceIds]));
      if (existing.confidence !== "high" && confidence === "high") existing.confidence = "high";
    }

    return modules.get(id);
  }

  for (const material of materials.filter((item) => item.type === "mock_screenshot")) {
    const text = texts.get(material.id) ?? "";
    const screenTitle = material.signals?.screenTitle || material.fileName;
    const module = ensureModule(screenTitle, material, "medium");
    const pageId = `page_${slug(screenTitle)}`;
    if (!pages.some((page) => page.id === pageId)) {
      const actions = [
        ...getBulletsUnderAny(text, ["Visible actions", "Actions"]),
        ...getBulletsUnderAny(text, ["Visible top actions", "Top actions"]),
        ...getBulletsUnderAny(text, ["Visible row actions", "Row actions"])
      ].map((action) => slug(action).replace(/^_+|_+$/g, "") || "view");

      pages.push({
        id: pageId,
        moduleId: module.id,
        name: splitBilingualLabel(screenTitle).name,
        nameZh: splitBilingualLabel(screenTitle).nameZh,
        purpose: `Draft page candidate detected from ${material.path}.`,
        mainActions: Array.from(new Set(actions.length ? actions : ["view"])).slice(0, 8),
        visibleFields: [],
        evidenceIds: evidenceByMaterial.get(material.id) ?? [],
        confidence: "high",
        needsReview: true
      });
      module.pages.push(pageId);
    }

    for (const navItem of getBulletsUnderAny(text, ["Visible navigation", "Navigation"])) {
      const navModule = ensureModule(navItem, material, "medium");
      const navPageId = `page_${slug(splitBilingualLabel(navItem).name)}`;
      if (!pages.some((page) => page.id === navPageId)) {
        pages.push({
          id: navPageId,
          moduleId: navModule.id,
          name: splitBilingualLabel(navItem).name,
          nameZh: splitBilingualLabel(navItem).nameZh,
          purpose: `Draft page candidate detected from navigation item "${navItem}".`,
          mainActions: ["view"],
          visibleFields: [],
          evidenceIds: evidenceByMaterial.get(material.id) ?? [],
          confidence: "medium",
          needsReview: true
        });
        navModule.pages.push(navPageId);
      }
    }
  }

  return { modules: Array.from(modules.values()), pages };
}

function buildEntitiesAndFields({ materials, texts, evidenceByMaterial }) {
  const entities = [];
  const fields = [];
  const csvMaterials = materials.filter((item) => item.type === "csv");
  const csvEntityNames = new Set(csvMaterials.map((material) => summarizeCsvEntity(material.path, texts.get(material.id) ?? "").entityName));

  for (const material of csvMaterials) {
    const csvSummary = summarizeCsvEntity(material.path, texts.get(material.id) ?? "");
    const entityName = csvSummary.entityName;
    const entityId = `ent_${slug(entityName)}`;
    const fieldIds = [];
    const relations = [];

    for (const fieldSummary of csvSummary.fields) {
      const header = fieldSummary.name;
      const fieldId = `field_${slug(entityName)}_${slug(header)}`;
      fieldIds.push(fieldId);
      const schemaType = fieldSummary.inferredType === "id" ? "string" : fieldSummary.inferredType === "reference" ? "relation" : fieldSummary.inferredType;
      const fieldRelationshipHint = fieldSummary.relationshipHint;
      const entityRelationshipHint = fieldRelationshipHint && csvEntityNames.has(fieldRelationshipHint.targetEntityName)
        ? fieldSummary.relationshipHint
        : null;
      fields.push({
        id: fieldId,
        entityId,
        name: header,
        nameZh: header,
        type: schemaType,
        required: fieldSummary.requiredHint === "likely_required" ? true : "unknown",
        examples: fieldSummary.sampleValues,
        possibleValues: fieldSummary.observedValues,
        source: material.path,
        semanticHint: fieldSummary.semanticRole,
        requiredHint: fieldSummary.requiredHint,
        relationshipHint: fieldRelationshipHint,
        confidence: "medium",
        needsReview: true,
        evidenceIds: evidenceByMaterial.get(material.id) ?? []
      });

      if (entityRelationshipHint) {
        const targetEntityId = `ent_${slug(entityRelationshipHint.targetEntityName)}`;
        if (!relations.some((relation) => relation.targetEntityId === targetEntityId && relation.sourceFieldId === fieldId)) {
          relations.push({
            type: "possible_reference",
            sourceFieldId: fieldId,
            targetEntityId,
            confidence: "low",
            needsReview: true,
            description: entityRelationshipHint.description
          });
        }
      }
    }

    entities.push({
      id: entityId,
      name: entityName,
      nameZh: entityName,
      description: `Entity candidate inferred from CSV file ${material.path}.`,
      fields: fieldIds,
      relations,
      evidenceIds: evidenceByMaterial.get(material.id) ?? [],
      confidence: "medium",
      needsReview: true
    });
  }

  return { entities, fields };
}

function ensureEntity(entities, fields, entityName, fieldNames, evidenceIds) {
  const entityId = `ent_${slug(entityName)}`;
  if (entities.some((entity) => entity.id === entityId)) return entityId;

  const fieldIds = fieldNames.map((fieldName) => {
    const fieldId = `field_${slug(entityName)}_${slug(fieldName)}`;
    fields.push({
      id: fieldId,
      entityId,
      name: fieldName,
      nameZh: fieldName,
      type: inferFieldType(fieldName),
      required: "unknown",
      examples: [],
      source: "deterministic_inventory",
      confidence: "low",
      needsReview: true,
      evidenceIds
    });
    return fieldId;
  });

  entities.push({
    id: entityId,
    name: entityName,
    nameZh: entityName,
    description: `Entity candidate inferred from deterministic inventory signals.`,
    fields: fieldIds,
    relations: [],
    evidenceIds,
    confidence: "low",
    needsReview: true
  });

  return entityId;
}

function buildRolesAndPermissions({ materials, texts, evidenceByMaterial }) {
  const roles = new Map();
  const permissions = [];

  function ensureRole(rawRole, material) {
    const label = splitBilingualLabel(rawRole);
    const roleId = `role_${slug(label.name)}`;
    const evidenceIds = evidenceByMaterial.get(material.id) ?? [];
    if (!roles.has(roleId)) {
      roles.set(roleId, {
        id: roleId,
        name: label.name,
        nameZh: label.nameZh,
        description: `Role candidate detected from ${material.path}.`,
        scope: inferRoleScope(`${label.name} ${label.nameZh}`),
        confidence: "medium",
        evidenceIds,
        needsReview: true
      });
    }
    return roles.get(roleId);
  }

  for (const material of materials) {
    const text = texts.get(material.id) ?? "";
    for (const roleLine of getBulletsUnder(text, "Visible roles")) {
      ensureRole(roleLine, material);
    }
    for (const roleLine of getBulletsUnder(text, "Roles")) {
      ensureRole(roleLine, material);
    }

    for (const heading of material.signals?.headings ?? []) {
      if (isLikelyRoleHeading(heading)) {
        ensureRole(heading, material);
      }
    }
  }

  for (const material of materials.filter((item) => item.type === "markdown" || item.type === "text")) {
    const text = texts.get(material.id) ?? "";
    const lines = getLines(text).map((line) => line.trim());
    let currentRole = null;

    for (const line of lines) {
      const heading = line.match(/^##\s+(.+)$/);
      if (heading) {
        currentRole = isLikelyRoleHeading(heading[1]) ? ensureRole(heading[1], material) : null;
        continue;
      }
      if (!currentRole || !line.startsWith("- ")) continue;

      const action = inferPermissionAction(line);
      if (action === "unknown") continue;
      const id = `perm_${slug(currentRole.name)}_${action}_${permissions.length + 1}`;
      const risky = /unclear|may|should be confirmed|不清楚|不确定|需要确认/i.test(line) || /export|approve|configure|delete/i.test(action);
      permissions.push({
        id,
        roleId: currentRole.id,
        resourceType: "DetectedResource",
        action,
        dataScope: currentRole.scope === "global" ? "all" : currentRole.scope === "custom" ? "assigned" : currentRole.scope,
        condition: line.replace(/^- /, ""),
        confidence: risky ? "low" : "medium",
        evidenceIds: evidenceByMaterial.get(material.id) ?? [],
        needsReview: true
      });
    }
  }

  if (roles.size === 0) {
    roles.set("role_unknown", {
      id: "role_unknown",
      name: "Unknown Role",
      nameZh: "未知角色",
      description: "Fallback role added because no explicit role material was detected.",
      scope: "unknown",
      confidence: "unknown",
      evidenceIds: [],
      needsReview: true
    });
  }

  if (permissions.length === 0) {
    const firstRole = Array.from(roles.values())[0];
    permissions.push({
      id: `perm_${slug(firstRole.name)}_unknown`,
      roleId: firstRole.id,
      resourceType: "DetectedResource",
      action: "unknown",
      dataScope: firstRole.scope === "global" ? "all" : firstRole.scope === "custom" ? "assigned" : firstRole.scope,
      condition: "Permission rules were not explicit in the deterministic inventory.",
      confidence: "unknown",
      evidenceIds: firstRole.evidenceIds,
      needsReview: true
    });
  }

  return { roles: Array.from(roles.values()), permissions };
}

function buildWorkflow({ materials, texts, roles, evidenceByMaterial }) {
  const states = new Set();
  const evidenceIds = [];

  for (const material of materials) {
    const text = texts.get(material.id) ?? "";
    for (const status of getBulletsUnderAny(text, ["Visible status labels", "Status labels", "Known states"])) {
      states.add(normalizeWorkflowStateLabel(status));
      evidenceIds.push(...(evidenceByMaterial.get(material.id) ?? []));
    }
    for (const state of extractWorkflowStatesFromText(text)) {
      states.add(state);
      evidenceIds.push(...(evidenceByMaterial.get(material.id) ?? []));
    }
    if (/Draft\s*[-→>]+\s*Submitted\s*[-→>]+\s*Reviewed\s*[-→>]+\s*Approved/i.test(text)) {
      ["draft", "submitted", "reviewed", "approved", "rejected"].forEach((state) => states.add(state));
      evidenceIds.push(...(evidenceByMaterial.get(material.id) ?? []));
    }
    if (material.type === "csv" && (material.signals?.headers ?? []).includes("status")) {
      for (const value of getCsvColumnValues(text, "status")) {
        states.add(normalizeWorkflowStateLabel(value));
      }
      evidenceIds.push(...(evidenceByMaterial.get(material.id) ?? []));
    }
  }

  const normalizedStates = Array.from(states);
  const actorIds = roles.slice(0, 3).map((role) => role.id);
  const fallbackActor = actorIds[0] ?? "role_unknown";
  const knownPath = ["draft", "submitted", "reviewed", "approved"].every((state) => normalizedStates.includes(state));

  return [{
    id: "wf_deterministic_review",
    name: "Deterministic Review Workflow",
    nameZh: "确定性复核流程草案",
    trigger: "Detected records or workflow notes indicate a possible review process.",
    actors: actorIds.length ? actorIds : [fallbackActor],
    steps: knownPath
      ? [
          { id: "step_draft_to_submitted", name: "Submit draft", actor: fallbackActor, action: "submit", fromState: "draft", toState: "submitted", confidence: "medium" },
          { id: "step_submitted_to_reviewed", name: "Review submitted record", actor: actorIds[1] ?? fallbackActor, action: "review", fromState: "submitted", toState: "reviewed", confidence: "medium" },
          { id: "step_reviewed_to_approved", name: "Approve reviewed record", actor: actorIds[2] ?? fallbackActor, action: "approve", fromState: "reviewed", toState: "approved", confidence: "low" }
        ]
      : [
          { id: "step_unknown_review", name: "Review detected record", actor: fallbackActor, action: "review", fromState: "unknown", toState: "unknown", confidence: "low" }
        ],
    states: normalizedStates.length ? normalizedStates : ["unknown"],
    evidenceIds: Array.from(new Set(evidenceIds)),
    confidence: knownPath ? "medium" : "low",
    needsReview: true
  }];
}

function buildQuestions({ materials, texts, permissions, workflows, evidenceByMaterial }) {
  const questions = [];
  const seen = new Set();

  function addQuestion(statement, material, fallbackBlockedItem) {
    const key = statement.toLowerCase();
    if (seen.has(key)) return;
    seen.add(key);

    const category = inferQuestionCategory(statement);
    questions.push({
      id: `q_${String(questions.length + 1).padStart(3, "0")}_${slug(statement).slice(0, 32)}`,
      question: statement.endsWith("?") ? statement : `Please confirm: ${statement}`,
      questionZh: statement,
      category,
      priority: inferQuestionPriority(statement),
      blockedItems: [fallbackBlockedItem].filter(Boolean),
      suggestedOwner: category === "permission" ? "business_owner" : category === "audit" ? "admin" : "product_manager",
      whyItMatters: "This item was flagged by deterministic rules as unclear or high-risk and affects draft implementation decisions.",
      evidenceIds: material ? evidenceByMaterial.get(material.id) ?? [] : []
    });
  }

  for (const material of materials) {
    const text = texts.get(material.id) ?? "";
    for (const line of getUnclearLines(text)) {
      addQuestion(line, material, workflows[0]?.id);
    }
  }

  for (const permission of permissions) {
    if (permission.needsReview && /export|approve|configure|delete|unknown/.test(permission.action)) {
      addQuestion(`Who can ${permission.action} ${permission.resourceType}?`, null, permission.id);
    }
  }

  if (questions.length === 0) {
    addQuestion("Which permissions, workflow states, and data scopes require business confirmation?", null, workflows[0]?.id);
  }

  return questions;
}

function attachQuestionRefs({ permissions, workflows, questions }) {
  const highRiskQuestions = questions.filter((question) => question.priority === "high").map((question) => question.id);
  for (const permission of permissions) {
    if (permission.needsReview && highRiskQuestions.length > 0) {
      permission.openQuestionIds = highRiskQuestions.slice(0, 2);
    }
  }
  for (const workflow of workflows) {
    workflow.openQuestionIds = questions
      .filter((question) => question.category === "workflow" || question.category === "permission")
      .slice(0, 4)
      .map((question) => question.id);
  }
}

function buildAssumptions({ questions, evidenceIds }) {
  const relatedQuestionIds = questions.slice(0, 2).map((question) => question.id);
  return [{
    id: "asm_deterministic_draft_requires_review",
    statement: "The generated draft is a conservative baseline and may omit or misclassify business rules.",
    statementZh: "自动生成的草案只是保守基线，可能遗漏或误分类业务规则。",
    reason: "Phase 5 uses deterministic file and text heuristics only, without AI, OCR, or business confirmation.",
    confidence: "low",
    needsReview: true,
    relatedQuestionIds,
    evidenceIds
  }];
}

function buildAcceptance({ modules, entities, permissions, workflows, questions }) {
  const criteria = [];
  const firstQuestion = questions[0]?.id;

  if (modules[0]) {
    criteria.push({
      id: "ac_detected_modules_reviewed",
      title: "Detected modules are reviewed by a human",
      description: "All deterministically detected modules should be reviewed before implementation.",
      category: "module",
      priority: "medium",
      relatedModuleIds: modules.slice(0, 3).map((module) => module.id),
      relatedQuestionIds: firstQuestion ? [firstQuestion] : [],
      confidence: "medium",
      needsReview: true,
      evidenceIds: modules[0].evidenceIds
    });
  }

  if (entities[0]) {
    criteria.push({
      id: "ac_detected_entities_fields_reviewed",
      title: "Detected entities and fields are reviewed",
      description: "CSV-derived entities and fields should be checked against source business meaning.",
      category: "entity",
      priority: "high",
      relatedModuleIds: modules.slice(0, 1).map((module) => module.id),
      relatedQuestionIds: firstQuestion ? [firstQuestion] : [],
      confidence: "medium",
      needsReview: true,
      evidenceIds: entities[0].evidenceIds
    });
  }

  if (permissions[0]) {
    criteria.push({
      id: "ac_permission_rules_confirmed",
      title: "Permission rules are confirmed",
      description: "Detected role and permission candidates must be confirmed before coding access control.",
      category: "permission",
      priority: "high",
      relatedModuleIds: modules.slice(0, 3).map((module) => module.id),
      relatedQuestionIds: questions.filter((question) => question.category === "permission").slice(0, 3).map((question) => question.id),
      confidence: "low",
      needsReview: true,
      evidenceIds: permissions[0].evidenceIds
    });
  }

  if (workflows[0]) {
    criteria.push({
      id: "ac_workflow_transitions_confirmed",
      title: "Workflow transitions are confirmed",
      description: "Detected workflow states and transitions should not be treated as final until reviewed.",
      category: "workflow",
      priority: "high",
      relatedModuleIds: modules.slice(0, 3).map((module) => module.id),
      relatedQuestionIds: questions.filter((question) => question.category === "workflow").slice(0, 3).map((question) => question.id),
      confidence: "low",
      needsReview: true,
      evidenceIds: workflows[0].evidenceIds
    });
  }

  return criteria;
}

function buildClaimMappings({ modules, pages, entities, fields, roles, permissions, workflows, acceptanceCriteria, openQuestions, assumptions }) {
  const claimGroups = [
    ["module", modules],
    ["page", pages],
    ["entity", entities],
    ["field", fields],
    ["permission", roles],
    ["permission", permissions],
    ["workflow", workflows],
    ["acceptance", acceptanceCriteria],
    ["question", openQuestions],
    ["assumption", assumptions]
  ];

  return claimGroups.flatMap(([claimType, items]) =>
    items.map((item) => ({
      claimId: item.id,
      claimType,
      claim: item.title || item.question || item.statement || item.name || item.id,
      evidenceIds: item.evidenceIds ?? [],
      confidence: item.confidence ?? "low",
      needsReview: item.needsReview ?? true,
      reasoningSummary: "Generated by deterministic Phase 5 draft rules from material inventory signals."
    }))
  );
}

function coverageSummary(claimMappings) {
  const totalClaims = claimMappings.length;
  const claimsWithEvidence = claimMappings.filter((mapping) => mapping.evidenceIds.length > 0).length;
  return {
    totalClaims,
    claimsWithEvidence,
    claimsWithoutEvidence: totalClaims - claimsWithEvidence,
    highConfidenceClaims: claimMappings.filter((mapping) => mapping.confidence === "high").length,
    mediumConfidenceClaims: claimMappings.filter((mapping) => mapping.confidence === "medium").length,
    lowConfidenceClaims: claimMappings.filter((mapping) => mapping.confidence === "low").length,
    unknownConfidenceClaims: claimMappings.filter((mapping) => mapping.confidence === "unknown").length,
    evidenceCoverageRate: totalClaims === 0 ? 0 : Number((claimsWithEvidence / totalClaims).toFixed(2))
  };
}

function buildability({ modules, entities, fields, roles, workflows, openQuestions, claimMappings }) {
  const evidenceRate = coverageSummary(claimMappings).evidenceCoverageRate;
  const dimensionScores = {
    moduleClarity: Math.min(13, 6 + modules.length),
    entityClarity: Math.min(13, 5 + entities.length),
    fieldClarity: Math.min(12, 5 + Math.floor(fields.length / 4)),
    permissionClarity: roles.length > 0 ? 8 : 4,
    workflowClarity: workflows[0]?.states?.length > 1 ? 9 : 5,
    evidenceCoverage: Math.max(7, Math.min(12, Math.round(evidenceRate * 12))),
    ambiguityControl: openQuestions.length > 0 ? 7 : 4
  };
  const score = Object.values(dimensionScores).reduce((sum, value) => sum + value, 0);

  return {
    score,
    status: "review_required",
    dimensionScores,
    mainBlockers: [
      "Deterministic draft requires human review",
      "Permission and workflow rules may be incomplete",
      "Open questions must be resolved before implementation"
    ],
    recommendedNextActions: [
      "Review detected modules and pages",
      "Confirm entities and field meanings with source owners",
      "Confirm role permissions and workflow transitions",
      "Regenerate or refine the spec pack after review"
    ]
  };
}

function assignPageFields(pages, fields) {
  const byEntity = new Map();
  for (const field of fields) {
    if (!byEntity.has(field.entityId)) byEntity.set(field.entityId, []);
    byEntity.get(field.entityId).push(field.id);
  }

  for (const page of pages) {
    const key = slug(page.name);
    let selected = [];
    if (/employee|staff/.test(key)) selected = fields.filter((field) => /employee/.test(field.id)).map((field) => field.id);
    if (/evaluation|review|record/.test(key)) selected = fields.filter((field) => /evaluation|record/.test(field.id)).map((field) => field.id);
    if (/permission/.test(key)) selected = fields.filter((field) => /permission|role/.test(field.id)).map((field) => field.id);
    page.visibleFields = (selected.length ? selected : fields.slice(0, 8).map((field) => field.id)).slice(0, 12);
  }
}

export function buildDraftSpecPack({ inventory, inputFolder }) {
  const texts = new Map();
  for (const material of inventory.materials) {
    texts.set(material.id, resolveText(inputFolder, material));
  }

  const supportedMaterials = inventory.materials.filter((material) => getMaterialSourceType(material.type));
  const sourceMaterials = createSourceMaterials(supportedMaterials);
  const { evidenceItems, evidenceByMaterial } = createEvidenceItems(supportedMaterials);
  const { modules, pages } = buildModuleAndPages({ materials: supportedMaterials, texts, evidenceByMaterial });
  const { entities, fields } = buildEntitiesAndFields({ materials: supportedMaterials, texts, evidenceByMaterial });
  const { roles, permissions } = buildRolesAndPermissions({ materials: supportedMaterials, texts, evidenceByMaterial });

  const roleEvidence = roles.flatMap((role) => role.evidenceIds).slice(0, 2);
  ensureEntity(entities, fields, "Role", ["role_id", "name", "scope"], roleEvidence);
  ensureEntity(entities, fields, "Permission", ["permission_id", "role_id", "resource_type", "action", "data_scope"], roleEvidence);
  if (supportedMaterials.some((material) => /audit|log|操作日志/i.test(`${material.summary} ${texts.get(material.id) ?? ""}`))) {
    ensureEntity(entities, fields, "AuditLog", ["audit_log_id", "record_id", "actor_id", "action", "action_at"], roleEvidence);
  }

  assignPageFields(pages, fields);
  const workflows = buildWorkflow({ materials: supportedMaterials, texts, roles, evidenceByMaterial });
  const openQuestions = buildQuestions({ materials: supportedMaterials, texts, permissions, workflows, evidenceByMaterial });
  attachQuestionRefs({ permissions, workflows, questions: openQuestions });
  const assumptions = buildAssumptions({ questions: openQuestions, evidenceIds: evidenceItems.slice(0, 1).map((item) => item.id) });
  const acceptanceCriteria = buildAcceptance({ modules, entities, permissions, workflows, questions: openQuestions });

  const generatedAt = new Date().toISOString();
  const claimMappings = buildClaimMappings({ modules, pages, entities, fields, roles, permissions, workflows, acceptanceCriteria, openQuestions, assumptions });
  const buildabilityReport = buildability({ modules, entities, fields, roles, workflows, openQuestions, claimMappings });

  const specPack = {
    schemaVersion: "0.1.0",
    project: {
      name: "Deterministic Draft Spec Pack",
      domain: "unknown",
      description: "Draft generated from deterministic material inventory heuristics. Human review is required.",
      primaryLanguage: "unknown",
      targetUsers: ["FDE", "AI full-stack developer", "business analyst"],
      status: "review_required"
    },
    sourceMaterials,
    modules,
    pages,
    entities,
    fields,
    roles,
    permissions,
    workflows,
    acceptanceCriteria,
    openQuestions,
    assumptions,
    buildability: buildabilityReport,
    handoff: {
      recommendedNextStep: "human_review_before_implementation",
      codingAgentReadiness: "review_required",
      suggestedImplementationOrder: [
        "Review deterministic draft",
        "Resolve high-priority open questions",
        "Confirm data models and permissions",
        "Regenerate final handoff after review"
      ],
      blockedByQuestions: openQuestions.slice(0, 5).map((question) => question.id)
    },
    generatedAt,
    generator: {
      name: "SpecWise deterministic draft generator",
      version: "0.1.0",
      mode: "deterministic_no_ai"
    }
  };

  const evidenceMap = {
    schemaVersion: "0.1.0",
    evidenceItems,
    claimMappings,
    coverageSummary: coverageSummary(claimMappings)
  };

  return {
    generatedNote: GENERATED_NOTE,
    generatedNoteZh: GENERATED_NOTE_ZH,
    specPack,
    evidenceMap
  };
}
